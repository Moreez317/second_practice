def f22(x):
    a = 0b00000000000000000000000000000001
    b = 0b00000000000000000000000000001110
    c = 0b00000000000000001111111111110000
    d = 0b00001111111111110000000000000000
    e = 0b11110000000000000000000000000000

    new_a = (x & a) << 15
    new_b = (x & b) >> 1
    new_c = (x & c) >> 1
    new_d = (x & d) << 4
    new_e = (x & e) >> 12

    return new_a | new_b | new_c | new_d | new_e

